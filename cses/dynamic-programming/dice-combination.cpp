constexpr ll MOD = (ll)(1e9) + 7;

auto solve() -> void
{
    ll n;
    cin >> n;

    vector<ll> ways(n + 1, 0);
    ways[0] = 1;
    ways[1] = 1;

    for (ll i = 2; i < n + 1; ++i) {
        for (ll j = 1; j < 7; ++j) {
            ll dif = i - j;
            if (dif >= 0) {
                ways[i] = (ways[i] + ways[dif]) % MOD;
            }
        }
    }

    cout << ways[n] << '\n';
}

auto main(int, char **) -> int
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int tt = 1;
    // cin >> tt;
    for (int t = 1; t <= tt; ++t) {
#ifdef LOCAL
        cerr << "+++ CASE: #" << t << " START\n";
#endif
        solve();
#ifdef LOCAL
        cerr << "+++ CASE: #" << t << " END\n";
        cerr << "+++ Time: " << setprecision(6) << elapsed_time() << " s\n";
        cerr << "@@@@@@@@@@@@@@@@@@@@\n";
#endif
    }

    return 0;
}
